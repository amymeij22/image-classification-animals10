def print_images_resolution(directory: Path, max_images_per_class: int | None = 200):

    unique_sizes = set()
    total_images = 0

    for subdir in sorted([d for d in directory.iterdir() if d.is_dir()]):
        image_files = list(iter_image_files(subdir))
        num_images = len(image_files)
        print(f"{subdir.name}: {num_images}")
        total_images += num_images

        files_to_scan = image_files if max_images_per_class is None else image_files[:max_images_per_class]
        for img_path in files_to_scan:
            try:
                with Image.open(img_path) as img:
                    unique_sizes.add(img.size)
            except (UnidentifiedImageError, OSError, ValueError):
                continue

    print("\nJumlah resolusi unik:", len(unique_sizes))
    for size in list(sorted(unique_sizes))[:15]:
        print(f"- {size}")

    print(f"\nTotal gambar (semua kelas): {total_images}")


@dataclass
class AuditResult:
    total_files: int
    corrupt_files: list
    sampled: int
    unique_resolutions: int
    resolution_examples: list


def audit_dataset_resolution_and_corrupt(dataset_dir: Path, sample_size: int = 400) -> AuditResult:
    all_files = []
    for class_dir in [d for d in dataset_dir.iterdir() if d.is_dir()]:
        all_files.extend(list(iter_image_files(class_dir)))

    if not all_files:
        return AuditResult(total_files=0, corrupt_files=[], sampled=0, unique_resolutions=0, resolution_examples=[])

    sample = random.sample(all_files, k=min(sample_size, len(all_files)))

    corrupt = []
    resolutions = []

    for p in sample:
        try:
            with Image.open(p) as im:
                im.verify()
            with Image.open(p) as im:
                resolutions.append(im.size)  # (width, height)
        except (UnidentifiedImageError, OSError, ValueError):
            corrupt.append(p)

    counter = Counter(resolutions)
    examples = counter.most_common(10)

    return AuditResult(
        total_files=len(all_files),
        corrupt_files=corrupt,
        sampled=len(sample),
        unique_resolutions=len(counter),
        resolution_examples=examples,
    )


def delete_files(files: list[Path]) -> int:
    deleted = 0
    for p in files:
        try:
            p.unlink(missing_ok=True)
            deleted += 1
        except OSError:
            pass
    return deleted


