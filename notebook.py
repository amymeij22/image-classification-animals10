def prepare_for_training(ds: tf.data.Dataset, training: bool, cache_name: str):
    if training:
        ds = ds.map(
            lambda x, y: (data_augmentation(x, training=True), y),
            num_parallel_calls=tf.data.AUTOTUNE,
        )
    ds = ds.map(lambda x, y: (normalization_layer(x), y), num_parallel_calls=tf.data.AUTOTUNE)

    cache_path = (CACHE_DIR / cache_name).as_posix()
    return ds.cache(cache_path).prefetch(tf.data.AUTOTUNE)


